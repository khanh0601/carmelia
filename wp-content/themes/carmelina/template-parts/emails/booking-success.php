			<h3>II. WHAT’S THE WEATHER LIKE IN HO TRAM & WHAT TO PACK:</h3>
			<p style="margin: 0; margin-bottom:15px;line-height: 1.5;">Situated on the southern coast of Vietnam, Ho Tram has a tropical climate. Thus here you will find stable high temperatures year round, from 25 – 35ºC (77 – 95ºF). The daytime average temperature is about 30ºC.</p>
			<p style="margin: 0; margin-bottom:15px;line-height: 1.5;">South Vietnam has two seasons, the dry season and the rainy season. The rainy season extends from May to October with large amounts of rainfall, typically for 1 – 2 hours and on average 2 out of 3 days. The dry season in South Vietnam extends from mid-November to April, where there is limited rainfall and thus several days and weeks without rain.</p>
			<p style="margin: 0; margin-bottom:15px;line-height: 1.5;">The tropical climate makes it perfect for hitting the beach and soaking up some sun any time of the year, so be sure to pack high protection sunscreen, lightweight breathable clothes, swimwears and flip flops!</p>
			<p style="margin: 0; margin-bottom:15px;line-height: 1.5;">Laundry service is at your disposal.</p>
			
